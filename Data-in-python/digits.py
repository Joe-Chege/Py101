""" Numbers 
Python is one of the most powerful languages when it comes to manipulating numerical data.
The numericals imclude integers , floating points and complex numbers  """
# integers >> The integer data type is comprised of all the positive and negative whole numbers.
#The amount of memory an integer occupies depends on its value. 
#For example, 0 will take up 24 bytes whereas 1 would occupy 28 bytes

#Here is a good examples when it comes to integers

print(10)  # A positive integer
print(-3000)  # A negative integer

num = 123456789  # Assigning an integer to a variable
print(num)
num = -16000  # Assigning a new integer
print(num)

""" Floating Point Numbers
Floating-point numbers, or floats, refer to positive and negative decimal numbers.
A float occupies 24 bytes of memory. """

# Here is an example of floating point numbers

print(1.00000000005)  # A positive float
print(-85.6701)  # A negative float

flt_pt = 1.23456789
print(flt_pt)


"""  Complex Numbers
Python  supports complex numbers, or numbers made up of a real and an imaginary part.
It requires two values. The first one will be the real part of the complex number,
while the second value will be the imaginary part.
"""

# Here is an example of learnig complex numbers

print(complex(10, 20))  # Represents the complex number (10 + 20j)
print(complex(2.5, -18.2))  # Represents the complex number (2.5 - 18.2j)

complex_1 = complex(0, 2)
complex_2 = complex(2, 0)
print(complex_1)
print(complex_2)

